        </ul>

        <h3>September to December</h3>
        <ul>
          <li>Having decided the Key Stage, you wish to teach start your main placement in one of our partner schools. Joining at the start of term means that you really are part of the team.</li>
          <li>Take opportunities to observe teaching and training so that you start to understand how teachers set the right tone and atmosphere for learning in their classroom.</li>
          <li>Work alongside the teacher in the classroom learning the skills and putting them into practice, when you feel ready, with support from your professional mentor.</li> 
          <li>Meet with your SCITT professional tutor.</li>
          <li>Receive regular offsite training, often with your primary colleagues, exploring pedagogy and planning lessons, positive behaviour for learning, being a professional and essential subject knowledge.</li> 
          <li>Enjoy some early years immersion so that you understand the context of your choice of Key Stage.</li> 
          <li>Use the professional support to help you find employment including creating a personal statement and developing your interview techniques.</li>
