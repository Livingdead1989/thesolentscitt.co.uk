    <section class="row" id="howbecometeacher">
      <div class="columns">
        <h2>How do I become a Teacher?</h2>
        <p>To enrol you will need the following:</p>

        <ul class="vertical menu" data-accordion-menu>
          <li>
            <a href="#">Degree Requirements</a>
            <ul class="menu vertical nested">
              <ul>
                <li>You must hold a degree of a UK university, higher education institution or an acceptable equivalent at 2.2 or above.</li>
                <li>The degree should include a minimum content of 50% which is directly related to the subject which you are training to teach.</li>
                <li>The degree, or equivalent qualification, must comprise 300 HE credit points, 60 of which are at level 6 of QCF (foundation degrees must be supplemented with 60 credits at level 6)</li>
                <li>You must be able to demonstrate appropriate subject knowledge for the age range you intend to teach.</li>
                <li>If the degree is not in the subject specialism, you must hold an A Level in the subject or have considerable relevant industry experience.</li>
                <li>Applicants with a non-UK degree will be required to demonstrate equivalence with UK standards through NARIC.</li>
              </ul>
            </ul>
          </li>
          <li>
            <a href="#">GCSEs Requirements</a>
            <ul class="menu vertical nested">
              <ul>
                <li>You must be in possession of appropriate prior qualifications including GCSE grade C/4 or above in English (or English Language) and Mathematics. If you are training within Primary, you must also hold a GCSE grade C/4 or above in a science subject.</li>
                <li>If you are otherwise suitable for ITT but, for whatever reason, have not successfully achieved a GCSE grade C/4 you may be given an opportunity to show that you can nevertheless meet the required standard by taking an equivalence test. This is something we can facilitate for you.</li>
                <li>Applicants with non-UK secondary school qualifications will be required to demonstrate equivalence with UK GCSE standards through NARIC.</li>
              </ul>
            </ul>
          </li>
        </ul>
        <br>
        <p>All applicants must be able to provide original certificates of academic and equivalent qualifications.</p>
        <p>Numeracy and literacy skills will be assessed on interview and we will use this information to identify needs and put tailored support packages in place.</p>
      </div>
